function getGate(num) {
  return GATES_64[num] || null;
}

function getGatesByCenter(center) {
  return Object.entries(GATES_64)
    .filter(([_, g]) => g.merkez.toLowerCase().includes(center.toLowerCase()))
    .map(([num, g]) => ({ num: parseInt(num), ...g }));
}

function getGatesByCircuit(circuit) {
  return Object.entries(GATES_64)
    .filter(([_, g]) => g.devre === circuit)
    .map(([num, g]) => ({ num: parseInt(num), ...g }));
}

function getPartner(num) {
  const gate = GATES_64[num];
  return gate ? GATES_64[gate.partner] : null;
}

// ──────────────────────────────────────────────
// DOĞRULAMA
// ──────────────────────────────────────────────

function validate() {
  const required = [
    'name','iching','symbol','tema','group','partner',
    'merkez','devre','kanal',
    'shadow','donum','siddhi',
    'savunma','savunma_aciklama','savunma_ornek','donusum_yonu',
    'hatlar','holo_kisa','holo_uzun'
  ];
  let errors = [];
  for (let i = 1; i <= 64; i++) {
    if (!GATES_64[i]) { errors.push(`Kapı ${i}: EKSIK`); continue; }
    const g = GATES_64[i];
    required.forEach(f => { if (!g[f]) errors.push(`Kapı ${i}: '${f}' eksik`); });
    if (!g.hatlar || g.hatlar.length !== 6) errors.push(`Kapı ${i}: 6 hat gerekli`);
  }
  return errors;
}

